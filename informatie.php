<h2>Ouderavond</h2>
<p>
Via deze website kunt u aangeven met welke docenten u een gesprek wilt op de komende
ouderspreekuuravonden op 14 en 15 december.
Inloggen doet u met dezelfde gegevens waarmee u ook op het ouderportaal inlogt. 
</p>
<p>
Geachte ouder(s)/verzorger(s),
</p><p>
Het docententeam dat de lessen verzorgt voor uw zoon/dochter spreekt binnenkort over de schoolresultaten. Hoewel u dankzij het ouderportaal de mogelijkheid heeft om die resultaten tussentijds in te zien, ontvangt u deze week een rapportage, die gebaseerd is op het gemiddelde resultaat per vak op 30 november 08.00 uur. We willen u er nadrukkelijk op wijzen dat de rapportage gebaseerd is op het voortschrijdende gemiddelde van de tot dan toe behaalde cijfers. Mogelijk is het handig om voor het docentenspreekuur nog even de actuele stand op het ouderportaal te raadplegen. Voor de klassen 1 t/m 3 moet de door u ondertekende rapportage uiterlijk vrijdag 16 december bij de mentor ingeleverd worden.
</p><p>
Naar aanleiding van de bespreking van de schoolresultaten nodigen wij u graag uit voor het docentenspreekuur op woensdag 14 december of donderdag 15 december. Indien u &eacute;&eacute;n van beide avonden verhinderd bent, kunt u dit via de aanmelding kenbaar maken. Om organisatorische reden wordt het docentenspreekuur beide avonden voor alle afdelingen op de locatie aan de Bergweg gehouden. Op deze avond heeft u de gelegenheid om tijdens een gesprek (van 8 minuten) van gedachten te wisselen met de mentor en/of vakdocent van uw zoon of dochter. Als u van mening bent dat er een langer gesprek dient plaats te vinden, zouden wij u willen verzoeken om telefonisch contact op te nemen met de betreffende collega voor een andere afspraak op een ander moment.
</p><p>
Voor parkeerruimte kunt u terecht aan de Bergweg (hoofdingang), Koninginneweg 
(achterzijde) en de Kamperfoeliestraat.
</p><p>
U kunt op onderstaande wijze, uiterlijk maandag 5 december, aangeven welke mede-werkers (maximaal 3 per kind) u wenst te spreken. Ouder(s)/verzorger(s) die meerdere kinderen op onze school hebben, worden verzocht, indien gewenst, voor alle betreffende kinderen &eacute;&eacute;n gezamenlijke aanmelding te doen.
</p><p>
Aanmelden kan uitsluitend digitaal via webadres http://ostrealyceum.org met dezelfde inloggegevens als bij het ouderportaal. 
</p><p>
	
Met vriendelijke groet,
</p><p>
De afdelingsleiders
</p>
